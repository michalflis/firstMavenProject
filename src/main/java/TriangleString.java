public class TriangleString {

    private int width;
    private int height;

    public TriangleString(int width, int height) {

        this.width = width;
        this.height = height;

    }

    @Override
    public String toString() {
        return "Triangle{" +
                "width=" + width +
                ", height=" + height +
                '}';
    }

}
